//Delay
inline void Delay(int milliSeconds)
{
	usleep(milliSeconds * 1000);
}

//MilliSecs
inline int MilliSecs()
{
	struct timeval tv;
	gettimeofday(&tv, 0);
	return tv.tv_sec * 1000 + tv.tv_usec/1000;
}

